import { create } from "zustand";
import { subscribeWithSelector } from "zustand/middleware";
import type { 栽培ブロック, 週インデックス } from "../types";
import { ローカルストレージから読む, 栽培ブロック一覧を保存する } from "./ローカルストレージ";

type タイムラインストア = {
  栽培ブロック一覧: 栽培ブロック[];
  現在週: 週インデックス;
  ブロックを追加する: (ブロック: Omit<栽培ブロック, "id">) => void;
  ブロックを更新する: (id: string, 差分: Partial<栽培ブロック>) => void;
  ブロックを削除する: (id: string) => void;
  現在週を設定する: (週: 週インデックス) => void;
};

const 初期データ = ローカルストレージから読む();

const idを生成する = () => crypto.randomUUID();

const 今日の週インデックスを求める = (): 週インデックス => {
  const 今日 = new Date();
  const 月 = 今日.getMonth() + 1; // 1-12
  const 日 = 今日.getDate();
  const 週番号 = Math.floor((日 - 1) / 7);

  // 月ごとの開始インデックス (3月=0, 4月=4, ..., 2月=48)
  const 月別開始インデックス: Record<number, number> = {
    3: 0, 4: 4, 5: 9, 6: 13, 7: 17, 8: 22,
    9: 26, 10: 30, 11: 35, 12: 39, 1: 43, 2: 48,
  };
  return (月別開始インデックス[月] ?? 0) + 週番号;
};

export const タイムラインストアを使う = create<タイムラインストア>()(
  subscribeWithSelector((set, get) => ({
    栽培ブロック一覧: 初期データ.栽培ブロック一覧,
    現在週: 今日の週インデックスを求める(),

    ブロックを追加する: (新しいブロック) => {
      const ブロック: 栽培ブロック = { ...新しいブロック, id: idを生成する() };
      set((状態) => ({ 栽培ブロック一覧: [...状態.栽培ブロック一覧, ブロック] }));
      栽培ブロック一覧を保存する(get().栽培ブロック一覧);
    },

    ブロックを更新する: (id, 差分) => {
      set((状態) => ({
        栽培ブロック一覧: 状態.栽培ブロック一覧.map((ブロック) =>
          ブロック.id === id ? { ...ブロック, ...差分 } : ブロック
        ),
      }));
      栽培ブロック一覧を保存する(get().栽培ブロック一覧);
    },

    ブロックを削除する: (id) => {
      set((状態) => ({
        栽培ブロック一覧: 状態.栽培ブロック一覧.filter((ブロック) => ブロック.id !== id),
      }));
      栽培ブロック一覧を保存する(get().栽培ブロック一覧);
    },

    現在週を設定する: (週) => set({ 現在週: 週 }),
  }))
);
